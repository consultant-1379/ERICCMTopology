package com.ericsson.counter.analytics.model.view;

public class ServiceImpactCounterDataView {
	private String datetime_id;
	private String cellName;
	private String counterName;
	private String counterValue;
	private String eventName;
	private String parameter;
	/**
	 * @return the datetime_id
	 */
	public String getDatetime_id() {
		return datetime_id;
	}
	/**
	 * @param datetime_id the datetime_id to set
	 */
	public void setDatetime_id(String datetime_id) {
		this.datetime_id = datetime_id;
	}
	/**
	 * @return the cellName
	 */
	public String getCellName() {
		return cellName;
	}
	/**
	 * @param cellName the cellName to set
	 */
	public void setCellName(String cellName) {
		this.cellName = cellName;
	}
	/**
	 * @return the counterName
	 */
	public String getCounterName() {
		return counterName;
	}
	/**
	 * @param counterName the counterName to set
	 */
	public void setCounterName(String counterName) {
		this.counterName = counterName;
	}
	/**
	 * @return the counterValue
	 */
	public String getCounterValue() {
		return counterValue;
	}
	/**
	 * @param counterValue the counterValue to set
	 */
	public void setCounterValue(String counterValue) {
		this.counterValue = counterValue;
	}
	/**
	 * @return the eventName
	 */
	public String getEventName() {
		return eventName;
	}
	/**
	 * @param eventName the eventName to set
	 */
	public void setEventName(String eventName) {
		this.eventName = eventName;
	}
	/**
	 * @return the parameter
	 */
	public String getParameter() {
		return parameter;
	}
	/**
	 * @param parameter the parameter to set
	 */
	public void setParameter(String parameter) {
		this.parameter = parameter;
	}
	
	
	
	

}
